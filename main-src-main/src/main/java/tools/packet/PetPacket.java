public class PetPacket {

    public static final byte[] updatePet(Item item) {
        List<ModifyInventory> lMods = new ArrayList<>();
        lMods.add(new ModifyInventory(ModifyInventory.Types.REMOVE, item));
        lMods.add(new ModifyInventory(ModifyInventory.Types.ADD, item));
        return modifyInventory(true, lMods);
    }

    public static final byte[] showPet(MapleCharacter chr, MaplePet pet, boolean remove, boolean hunger) {
        return showPet(chr, pet, remove, hunger, false);
    }

    public static final byte[] showPet(MapleCharacter chr, MaplePet pet, boolean remove, boolean hunger, boolean show) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(
                show ? SendPacketOpcode.LP_PetModified.getValue() : SendPacketOpcode.LP_PetActivated.getValue());
        mplew.writeInt(chr.getId());
        mplew.writeInt(chr.getPetIndex(pet));
        mplew.write(remove ? 0 : 1);
        mplew.write(hunger ? 1 : 0);
        if (!remove) {
            addPetInfo(mplew, chr, pet, false);
        }
        return mplew.getPacket();
    }

    public static final byte[] removePet(int cid, int index) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetActivated.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(index);
        mplew.writeShort(0);

        return mplew.getPacket();
    }

    public static byte[] movePet(int cid, int slot, int duration, Point mPos, Point oPos,
            List<LifeMovementFragment> moves) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetMove.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(slot);
        PacketHelper.serializeMovementList(mplew, duration, mPos, oPos, moves);

        return mplew.getPacket();
    }

    public static byte[] petChat(int cid, int un, String text, byte slot) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetActionSpeak.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(slot);
        mplew.write(un);
        mplew.write(0);
        mplew.writeMapleAsciiString(text);

        return mplew.getPacket();
    }

    public static byte[] petColor(int cid, byte slot, int color) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetHueChanged.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(slot);
        mplew.writeInt(color);

        return mplew.getPacket();
    }

    public static final byte[] commandResponse(int cid, byte command, byte slot, boolean success, boolean food) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetActionCommand.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(slot);
        mplew.write(command == 1 ? 2 : 1);
        if (command != 1) {
            mplew.write(command);
            mplew.write(success ? 1 : 0);
            mplew.write(0);
        } else {
            mplew.write(1);
            mplew.writeInt(0);
        }

        return mplew.getPacket();
    }

    public static final byte[] petSize(int cid, byte slot, short size) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetSizeChange.getValue());
        mplew.writeInt(cid);
        mplew.writeInt(slot);
        mplew.writeShort(size);
        mplew.write(0);
        mplew.write(0);

        return mplew.getPacket();
    }

    public static final byte[] showPetUpdate(MapleCharacter chr, int uniqueId, byte index) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_PetLoadExceptionList.getValue());
        mplew.writeInt(chr.getId());
        mplew.writeInt(index);
        mplew.writeLong(uniqueId);
        mplew.write(0); // > 0 => + [Int]

        return mplew.getPacket();
    }

    public static byte[] petStatUpdate(MapleCharacter chr) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_StatChanged.getValue());
        mplew.write(0);
        mplew.writeLong(MapleStat.PET.getValue());
        byte count = 0;
        for (MaplePet pet : chr.getSummonedPets()) {
            mplew.writeLong(pet.getUniqueId());
            count = (byte) (count + 1);
        }
        while (count < 3) {
            mplew.writeZeroBytes(8);
            count = (byte) (count + 1);
        }
        mplew.write(0);
        mplew.writeShort(0);
        mplew.writeZeroBytes(100);

        return mplew.getPacket();
    }

    public static void addPetInfo(MaplePacketLittleEndianWriter mplew, MapleCharacter chr, MaplePet pet,
            boolean showpet) {
        if (showpet) {
            mplew.write(1);
            mplew.writeInt(chr.getPetIndex(pet));
        }
        mplew.writeInt(pet.getPetItemId());
        mplew.writeMapleAsciiString(pet.getName());
        mplew.writeLong(pet.getUniqueId());
        mplew.writePos(pet.getPos());
        mplew.write(pet.getStance());
        mplew.writeShort(pet.getFh());
        mplew.writeInt(-1);
        mplew.writeShort(100); // 
        mplew.write(0);
        mplew.write(0);
    }

    public static void addPet(MaplePacketLittleEndianWriter mplew, MapleCharacter chr, MaplePet pet) {
        if (pet.getPetItemId() == 0) {
            mplew.writeInt(0);
        } else {
            mplew.writeInt(pet.getPetItemId());
        }
    }

    public static byte[] showPetPickUpMsg(boolean canPickup, int pets) {
        MaplePacketLittleEndianWriter mplew = new MaplePacketLittleEndianWriter();

        mplew.writeShort(SendPacketOpcode.LP_CashPetSkillSettingResult.getValue());
        mplew.write(canPickup ? 1 : 0);
        mplew.write(pets);

        return mplew.getPacket();
    }
}
